#include "../philo/philo.h"
#include "../philo_bonus/philo_bonus.h"

int	ft_iswhitespace(int c)
{
	return (c == ' ' || c == '\t' || c == '\n' || c == '\r'
		|| c == '\v' || c == '\f');
}
